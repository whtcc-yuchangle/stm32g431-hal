{
    TIM16->CCR1 = tim16_duty;
    TIM17->CCR1 = tim17_duty;
    
    HAL_TIM_PWM_Start(&htim16, TIM_CHANNEL_1);
    HAL_TIM_PWM_Start(&htim17, TIM_CHANNEL_1);
}

void pwm_test(void)
{
    uint8_t key_down = 0;
    
    key_scan(&key_down);
    
    if (key_down == 2)
    {
        tim16_duty += 10;
        if (tim16_duty > 90)
        {
            tim16_duty = 10;
        }
        TIM16->CCR1 = tim16_duty;
    }
    else if (key_down == 3)
    {
        tim17_duty += 10;
        if (tim17_duty > 90)
        {
            tim17_duty = 10;
        }
        TIM17->CCR1 = tim17_duty;
    }
}

void tim_input_capture_start(void)
{
    HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_1);
    HAL_TIM_IC_Start_IT(&htim2, TIM_CHANNEL_2);
    HAL_TIM_IC_Start_IT(&htim3, TIM_CHANNEL_1);
    HAL_TIM_IC_Start_IT(&htim3, TIM_CHANNEL_2);
}

void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
    uint32_t tim2_cap_up = 0;
    uint32_t tim2_cap_down = 0;
    uint32_t tim3_cap_up = 0;
    uint32_t tim3_cap_down = 0;
    
    if (htim->Instance == TIM2)
    {
        if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
        {
            tim2_cap_up = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
            tim2_cap_down = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);
            
            TIM2->CNT = 0;
            
            tim2_duty = ((tim2_cap_down+1)*1.0) / (tim2_cap_up*1.0) * 100;
            tim2_cap_hz = HAL_RCC_GetHCLKFreq() / (htim->Init.Prescaler + 1) / tim2_cap_up;
            
            HAL_TIM_IC_Start_IT(htim, TIM_CHANNEL_1);
            HAL_TIM_IC_Start_IT(htim, TIM_CHANNEL_2);
        }
    }
    else if (htim->Instance == TIM3)
    {
        if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
        {
            tim3_cap_up = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
            tim3_cap_down = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);

            TIM3->CNT = 0;

            tim3_duty = ((tim3_cap_down+1)*1.0) / (tim3_cap_up*1.0) * 100;
            tim3_cap_hz = HAL_RCC_GetHCLKFreq() / (htim->Init.Prescaler + 1) / tim3_cap_up;
            
            HAL_TIM_IC_Start_IT(htim, TIM_CHANNEL_1);
            HAL_TIM_IC_Start_IT(htim, TIM_CHANNEL_2);
        }
    }
    
   
}

int time_input_capture_get(TIM_HandleTypeDef *htim, uint8_t *duty, uint32_t *hz)
{
    if (htim == &htim2)
    {
        *duty = tim2_duty;
        *hz = tim2_cap_hz;
        return 0;
    }
    else if (htim == &htim3)
    {
        *duty = tim3_duty;
        *hz = tim3_cap_hz;
        return 0;
    }
    else
    {
        return -1;
    }
}

